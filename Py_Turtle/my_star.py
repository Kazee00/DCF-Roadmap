import turtle
distance=100
degree=144
my_turtle = turtle.Turtle()
my_turtle.forward(distance)  
my_turtle.right(degree)    
my_turtle.forward(distance)  
my_turtle.right(degree)    
my_turtle.forward(distance) 
my_turtle.right(degree)    
my_turtle.forward(distance)  
my_turtle.right(degree)    
my_turtle.forward(distance)  
turtle.done()