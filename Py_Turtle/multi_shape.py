import turtle
beast = turtle.Turtle()

beast.penup()
beast.goto(-300, 0)
beast.pendown()
beast.setheading(0)
beast.forward(120)

beast.penup()
beast.goto(-50, 0)
beast.pendown()
beast.setheading(0)
beast.forward(120)
beast.left(90)
beast.forward(100)

beast.penup()
beast.goto(200, 0)
beast.pendown()
beast.setheading(0)
beast.forward(120)
beast.left(120)
beast.forward(120)
beast.right(-120)
beast.forward(120)

turtle.done()